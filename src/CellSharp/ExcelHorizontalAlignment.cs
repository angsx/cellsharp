namespace CellSharp;

/// <summary>Specifies the horizontal alignment used for exported data cells.</summary>
public enum ExcelHorizontalAlignment
{
    /// <summary>Lets Excel select the alignment from the cell value type.</summary>
    General,
    /// <summary>Aligns cell content to the left.</summary>
    Left,
    /// <summary>Centers cell content horizontally.</summary>
    Center,
    /// <summary>Aligns cell content to the right.</summary>
    Right,
}
