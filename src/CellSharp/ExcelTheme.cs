namespace CellSharp;

/// <summary>Identifies a built-in workbook presentation theme.</summary>
public enum ExcelTheme
{
    /// <summary>Uses CellSharp's balanced default colors and typography.</summary>
    Default,
    /// <summary>Uses a stronger contemporary header treatment.</summary>
    Modern,
    /// <summary>Uses a conventional business workbook treatment.</summary>
    Classic,
    /// <summary>Uses a restrained low-decoration treatment.</summary>
    Minimal,
}
